package ReviewJava;

public class Concatenation {

	public static void main(String[] args) {
		
		int studentCount=259;
		System.out.print("studentCount is ");
		System.out.println(studentCount); 
		
		
		String sentence = "the student count is";
		System.out.println( sentence + studentCount);
		
		System.out.println("The student count is " + studentCount);
	
	
	
	}

}
