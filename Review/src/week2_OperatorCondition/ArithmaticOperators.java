		// Arithmatic Operators
		// + , - , * , / , %
		
		System.out.println( 9 + 5);
		System.out.println( 9 - 5);
		System.out.println( 9 * 5);
		
		// division between whole number always result in whole number (int)
		System.out.println( 9 / 5);
		// modulus or remainder operator will return the remaining part after division
		System.out.println( 9 % 5);
		
		System.out.println(10/3); // 3
		System.out.println(10%3); // 1

