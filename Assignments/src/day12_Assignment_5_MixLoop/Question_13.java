package day12_Assignment_5_MixLoop;

public class Question_13 {

	public static void main(String[] args) {
		
		for(int i=1; i<=4; i++) {
			for(int j=1; j<=4; j++) {
				System.out.print("W ");
				System.out.print("B ");
			}
			System.out.println();
			for(int j=1; j<=4; j++) {
				System.out.print("B ");
				System.out.print("W ");
			}
			System.out.println();
		}

		
		
	}

}
