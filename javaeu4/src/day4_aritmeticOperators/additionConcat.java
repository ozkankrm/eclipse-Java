package day4_aritmeticOperators;

public class additionConcat {

	public static void main(String[] args) {
		// Number + Number = Addition
		System.out.println(2+4);
		
		//Number + String : Concatenation
		
		System.out.println(5 + "hello");
		
		// String + boolean : Concatenation
		System.out.println("hello" + true);
		
		// String + String : Concatenation
		System.out.println("hello" + "world");
		
		// Boolena + number
		//System.out.println(true+1); // doesn't work
		
		System.out.println((2+0+3+"Cybertek"));
		System.out.println("Cybertek" +2+0+5);
		System.out.println(2+0+5+ "Cyberte"+2+0+5);
		System.out.println(2+0+5+ "Cyberte"+(2+0+1));
		System.out.println();
		

	}
 
}
